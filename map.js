function draw_map(canvas, ctx) {
    const div_x = canvas.width / map[0].length;
    const div_y = canvas.height / map.length;


    ctx.strokeStyle = "grey"

    for (let i = 0; i <= canvas.width; i = i + div_x) {
        ctx.moveTo(i, 0);
        ctx.lineTo(i, canvas.height);
        ctx.stroke();
    }


    for (let i = 0; i <= canvas.height; i = i + div_y) {
        ctx.moveTo(0, i);
        ctx.lineTo(canvas.width, i);
        ctx.stroke();
    }


    ctx.fillStyle = "white";

    for (let i = 0; i < map.length; i++) {

        for (let j = 0; j < map[i].length; j++) {
            pos_x = j * (div_x);
            pos_y = i * (div_y);

            if (map[i][j] == 1) {
                ctx.fillRect(pos_x, pos_y, div_x, div_y);
            }
        }
    }


    
}





